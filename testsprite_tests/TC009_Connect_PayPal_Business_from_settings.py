        # -> Fill the email and password fields ([1521], [1524]) with example@gmail.com and password123 (clearing first) and click the Sign In button ([1531]) to attempt authentication.
        # email input placeholder=" "
        elem = page.locator("xpath=/html/body/div[4]/div[2]/div/form/div/input").nth(0)
        await elem.wait_for(state="visible", timeout=10000)
        await elem.fill("example@gmail.com")
        
        # -> Fill the email and password fields ([1521], [1524]) with example@gmail.com and password123 (clearing first) and click the Sign In button ([1531]) to attempt authentication.
        # password input placeholder=" "
        elem = page.locator("xpath=/html/body/div[4]/div[2]/div/form/div[2]/input").nth(0)
        await elem.wait_for(state="visible", timeout=10000)
        await elem.fill("password123")
        
        # -> Fill the email and password fields ([1521], [1524]) with example@gmail.com and password123 (clearing first) and click the Sign In button ([1531]) to attempt authentication.
        # button "Sign In"
        elem = page.locator("xpath=/html/body/div[4]/div[2]/div/form/div[3]/button").nth(0)
        await elem.wait_for(state="visible", timeout=10000)
        await elem.click()
        
        # -> Fill the email and password fields (indexes 1626 and 1627) with example@gmail.com / password123 and click the Sign In button (index 1628).
        # email input placeholder=" "
        elem = page.locator("xpath=/html/body/div[4]/div[2]/div/form/div/input").nth(0)
        await elem.wait_for(state="visible", timeout=10000)
        await elem.fill("example@gmail.com")
        
